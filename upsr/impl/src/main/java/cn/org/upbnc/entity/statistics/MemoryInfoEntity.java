    private String position;
    private String entIndex;
    private String osMemoryTotal;
    private String osMemoryUse;
    private String osMemoryFree;
    private String osMemoryUsage;
    private String doMemoryTotal;
    private String doMemoryUse;
    private String doMemoryFree;
