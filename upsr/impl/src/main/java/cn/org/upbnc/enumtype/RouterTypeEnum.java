package cn.org.upbnc.enumtype;

public enum RouterTypeEnum {
    Router_P (0, "P"),
    Router_PE(1,"PE"),
    Router_CE(1,"CE");

    private int code;
    private String name;

    RouterTypeEnum(int code ,String name){
        this.code = code;
        this.name = name;
    }

    public int getCode() {
        return code;
    }

    public String getName() {
        return name;
    }
}
